import useSWR from 'swr';
import type { Prisma } from './types';
import { DefaultArgs, GetFindResult } from './types/runtime/library';
import { useRoqClient } from './roq-client-provider';
import { SWRRequestOptions, RequestOptions } from './roq-sdk';

export function useUniversityFindMany<
  T extends Prisma.universityFindManyArgs,
  R extends GetFindResult<Prisma.$universityPayload<DefaultArgs>, T>[],
>(
  args?: Prisma.SelectSubset<T, Prisma.universityFindManyArgs>,
  options?: RequestOptions,
  swrOptions?: SWRRequestOptions<R>,
) {
  const roq = useRoqClient();
  const key = JSON.stringify(['useUniversityFindMany', args || {}]);
  return useSWR<R, Error>(
    key,
    async () => {
      const result = await roq.university.findMany(args, options);
      return result as R;
    },
    {
      ...swrOptions,
      fallbackData: swrOptions?.initialData ?? swrOptions?.fallbackData,
    },
  );
}

